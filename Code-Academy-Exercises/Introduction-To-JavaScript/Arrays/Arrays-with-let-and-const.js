let condiments = ['Ketchup', 'Mustard', 'Soy Sauce', 'Sriracha'];

const utensils = ['Fork', 'Knife', 'Chopsticks', 'Spork'];

condiments[0] = "Mayo";
console.log(condiments);

condiments = ["Mayo"];
console.log(condiments);

utensils[3] = "Spoon";
console.log(utensils)

//This shows that we can completely reassign an entire array, as long as it's a "let" rather than "const"
